
// JAVASCRIPT (js) VS JAVA o C++ >> TYPESCRIPT (ts)
/**
 * La mayor diferencia entre js y otros lenguajes como Java o C++ es el tipado:
 * mientras que java es totalmente flexible a la hora de declarar variables
 * y reasignar los valores sin importar el tipo de dato que tenga, otros lenguajes, 
 * no permiten hacer eso ya que a la hora de crear la variable hay que especificarle
 * el tipo de dato que va a contener y eso no puede cambiar en el futuro.
 * 
 * La otra gran diferencia es la forma en la que se estructura el código,
 * mientras que JavaScript está orientado a funciones Java, C++ o .NET estan orientado a objetos
 * 
 * 
 * Estas problemas se solventan usando TypeScript que es un Lenguaje basado en JavaScript cuyo fin
 * es ayudar al programar a escribir un codigo más robusto y propenso a fallos aportando la
 * flexibilidad de javascript y la seguridad de los otros lenguajes con el tipado y la orientación
 * objetos. TypeScript no es interpretado por nadie si no que es traducido a JavaScript que lo que
 * los navegadores web o intepretadores van a finalmente ejecutar .
 *
 */

// TIPOS DE DATOS 
    // PRIMITIVOS
    true; false; // booleanos
    'texto'; // string
    123; // number
    null; // null
    undefined; // undefined
    NaN // Not a Number

    // COMPUESTOS
    
    ({ 
        nombre: 'Eustaquio',
        edad: 50,               // objetos
        esEspañol: true
    })


